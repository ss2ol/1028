
public class For_2 {

	public static void main(String[] args) {
		int i, j;
		for(i=0, j=0; j<3; i=i+1, j=j+1) {
			System.out.println("Hollo World");
			}
		// 두개 값 넣어도 가능 
		//두개의 변수는 ','로 구분 
		
		for (;;) {
			System.out.println("Hollo World");
		}
		//()안에 내용 생략하면 무한반복되거나 한번도 수행되지 않음
		
		
		
		
		
 }
}